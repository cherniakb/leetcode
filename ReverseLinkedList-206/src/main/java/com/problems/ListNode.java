package com.problems;

public class ListNode {
    public int val;
    public ListNode next;

    ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

    @Override
    public String toString() {
        return "com.problems.ListNode{" +
            "val=" + val +
            ", next=" + next +
            '}';
    }
}
